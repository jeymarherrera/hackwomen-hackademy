# Operador	Equivalencia
#   var += 1	var = var + 1
#   var -= 1	var = var - 1
#   var *= 1	var = var * 1
#   var /= 1	var = var / 1
#   var %= 1	var = var % 1

# Ejemplos:

saludo = 'Hello'
saludo += ' world!'
saludo
# 'Hello world!'

numero = 1
numero += 1
numero
# 2

my_list = ['item']
my_list *= 3

# my_list
# ['item', 'item', 'item']